// A name is at least one letter, usually followed by one or more letters, digits, or underbars. 
// A name cannot be one of these reserved words:
//		abstract
//		boolean  break  byte
//		case  catch  char  class  const  continue
//		debugger  default  delete  do  double
//		space
//		tab
//		line
//		end
//		else  enum  export  extends
//		false  final  finally  float  for  function
//		goto
//		if  implements  import  in  instanceof  int  interface
//		long
//		native  new  null
//		package  private  protected  public
//		return
//		short  static  super  switch  synchronized
//		this  throw  throws  transient  true  try  typeof
//		var  volatile  void
//		while  with
//		*  or  /  //  *  /  */
//		whitespace
//		numbers - not by themselves, though they can append other valid names. ex: myVar_2 and myVar2 are both valid.

// Most of the reserved words in this list are not used in the language. 
// The list does not include some words that should have been reserved but were not, such as 
//		undefined
//		NaN
//		Infinity
// It is not permitted to name a variable or parameter with a reserved word. 
// Worse, it is not permitted to use a reserved word as the name of an object
// property in an object literal or following a dot in a refinement.
// Names are used for statements, variables, parameters, property names, operators, and labels.
